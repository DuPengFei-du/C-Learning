//最大公约数
#include<stdio.h>
int main()
{
	int a = 0;
	int c = 1;
	int b = 0;
	(void)scanf("%d %d", &a, &b);
	while (c = a % b)
	{
		a = b;
		b = c;
	}
	printf("%d", b);
	return 0;
}


