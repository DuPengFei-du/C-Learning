void* my_memcpy(void* dest, const void* source, size_t num)
{
	//每次拷贝一个字节最为合适
	void* ret = dest;
	assert(dest != NULL);
	assert(source != NULL);
	while (num--)
	{
		*(char*)dest = *(char*)source;
		((char*)dest)++;
		((char*)source)++;
	}
	return ret;
}
