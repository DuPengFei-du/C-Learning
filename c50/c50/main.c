//猜数字游戏的实现
#include<stdio.h>
#include<stdlib.h>
#include<time.h>
void game()
{
	int guess = 0;
	int ret = 0;
	ret = rand() % 100 + 1;
	while (1)
	{
		printf("请输入:>\n");
		(void)scanf("%d", &guess);
		if (guess > ret)
			printf("猜大了\n");
		else if (guess < ret)
			printf("猜小了\n");
		else
		{
			printf("猜对了\n");
			break;
		}
	}
}
void menu()
{
	printf("*****************************\n");
	printf("     欢迎来到猜数字游戏！    \n");
	printf("           1.play            \n");
	printf("           0.exit            \n");
	printf("*****************************\n");
}
void test()
{
	int input = 0;
	srand((unsigned int)time(NULL));
	do
	{
		menu();
		printf("请选择:>\n");
		(void)scanf("%d", &input);
		switch (input)
		{
		case 1:
			game();
			break;
		case 0:
			printf("退出游戏!\n");
			break;
		default:
			printf("输入错误，请重新输入!\n");
			break;
		}
	} while (input);
}
int main()
{
	test();
	return 0;
}


