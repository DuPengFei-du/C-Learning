//如果希望追加的字符个数大于我字符串本身的个数的话，不会再像后面追加0.只会链接我字符串本身的长度，然后放一个\0，别的什么操作都没有


#include<stdio.h>
#include<assert.h>
char* my_strncat(char* dest, const char* src, int n)
{
	char* ret = dest;
	assert(dest);
	assert(src);
	char* p = dest;
	while (*p != 0)
	{
		p++;
	}
	while (n--)
	{
		*p++ = *src++;
	}
	*p = 0;  //主动追加一个\0就字符串连接了。
	return ret;
}
int main()
{
	char arr1[30] = "hello world";
	char arr2[] = "change world";
	my_strncat(arr1, arr2, 3);
	printf("%s", arr1);
	return 0;
}


