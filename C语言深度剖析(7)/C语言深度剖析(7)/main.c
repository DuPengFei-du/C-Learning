//左移和右移

//下面讨论一下左移和右移：
//左移运算符“ << ”是双目运算符。其功能把“ << ”左边的运算数的各二进位全部左移若干
//位，由“ << ”右边的数指定移动的位数，高位丢弃，低位补0。
//右移运算符“ >> ”是双目运算符。其功能是把“ >> ”
//左边的运算数的各二进位全部右移若干位，“ >> ”右边的数指定移动的位数。
//但注意：对于有符号数，在右移时，符号位将随同移动。
//当为正数时， 最高位补0；而为负数时，符号位为1，最高位是补0 或是补1 取决于编译系统的规定。
//Turbo C 和很多系统规定为补1。


